/** Deterministic PRNG (mulberry32) so conformance sampling is reproducible.
 *  Mirrors the fuzz-test rng in core/test — duplicated here because the kit
 *  ships in the library and cannot import from test/. */
export function rng(seed: number): () => number {
  let a = seed >>> 0;
  return () => {
    a |= 0;
    a = (a + 0x6d2b79f5) | 0;
    let t = Math.imul(a ^ (a >>> 15), 1 | a);
    t = (t + Math.imul(t ^ (t >>> 7), 61 | t)) ^ t;
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}
